const langButtons = document.querySelectorAll(".lang-button");
const featuresList = document.getElementById("featuresList");
const trailerContainer = document.getElementById("trailerContainer");
const communityLabel = document.getElementById("communityLabel");
const communityValue = document.getElementById("communityValue");
const logoImage = document.getElementById("logoImage");
const lightbox = document.getElementById("lightbox");
const lightboxImage = document.getElementById("lightboxImage");
const lightboxClose = document.getElementById("lightboxClose");
let currentLang = "en";

const logoImages = {
  en: "assets/screenshots/logo-en.png",
  zh: "assets/screenshots/logo-zh.png",
  ja: "assets/screenshots/logo-en.png"
};

function setTrailer(lang) {
  const provider = lang === "zh" ? trailerEmbeds.bilibili : trailerEmbeds.youtube;
  trailerContainer.innerHTML = "";

  const iframe = document.createElement("iframe");
  iframe.src = provider.src;
  iframe.title = provider.title;
  iframe.loading = "lazy";
  iframe.allow = "accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share";
  iframe.allowFullscreen = true;
  trailerContainer.appendChild(iframe);
}

function setCommunityContact(lang, strings) {
  communityValue.replaceChildren();

  if (lang === "zh") {
    communityLabel.textContent = strings.qqGroup;
    communityValue.textContent = QQ_GROUP_NUMBER;
    return;
  }

  const link = document.createElement("a");
  link.className = "contact-button";
  link.href = DISCORD_INVITE_URL;
  link.target = "_blank";
  link.rel = "noopener";
  link.textContent = strings.discordButton;

  communityLabel.textContent = strings.discord;
  communityValue.appendChild(link);
}

function applyLanguage(lang) {
  const strings = dictionary[lang] || dictionary.en;
  currentLang = lang;
  document.documentElement.lang = lang === "zh" ? "zh-CN" : lang;
  document.title = strings.pageTitle;

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (typeof strings[key] === "string") {
      node.textContent = strings[key];
    }
  });

  document.querySelectorAll("[data-i18n-attr]").forEach((node) => {
    node.dataset.i18nAttr.split(",").forEach((pair) => {
      const [attr, key] = pair.split(":");
      if (attr && key && typeof strings[key] === "string") {
        node.setAttribute(attr.trim(), strings[key]);
      }
    });
  });

  featuresList.replaceChildren();
  strings.features.forEach((feature) => {
    const item = document.createElement("li");
    item.textContent = feature;
    featuresList.appendChild(item);
  });

  if (logoImage) {
    logoImage.src = logoImages[lang] || logoImages.en;
  }

  langButtons.forEach((button) => {
    const isActive = button.dataset.lang === lang;
    button.classList.toggle("active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  setCommunityContact(lang, strings);
  setTrailer(lang);
}

function openLightbox(card) {
  const image = card.querySelector("img");
  const fullSrc = card.dataset.full || image.src;
  lightboxImage.src = fullSrc;
  lightboxImage.alt = image.alt;
  lightbox.hidden = false;
  document.body.style.overflow = "hidden";
  lightboxClose.focus();
}

function closeLightbox() {
  lightbox.hidden = true;
  lightboxImage.src = "";
  document.body.style.overflow = "";
}

langButtons.forEach((button) => {
  button.addEventListener("click", () => applyLanguage(button.dataset.lang));
});

document.querySelectorAll(".media-card").forEach((card) => {
  card.addEventListener("click", () => openLightbox(card));
});

document.querySelectorAll("img").forEach((image) => {
  if (image.complete && image.naturalWidth === 0) {
    image.classList.add("is-missing");
  }

  image.addEventListener("error", () => {
    image.classList.add("is-missing");
  });
});

lightboxClose.addEventListener("click", closeLightbox);

lightbox.addEventListener("click", (event) => {
  if (event.target === lightbox) {
    closeLightbox();
  }
});

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape" && !lightbox.hidden) {
    closeLightbox();
  }
});

applyLanguage(currentLang);
